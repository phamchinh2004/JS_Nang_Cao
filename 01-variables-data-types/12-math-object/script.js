let x;

// Square root
x = Math.sqrt(9);//Căn bậc hai

// Absolute value
x = Math.abs(-5);//Trả về giá trị tuyệt đối 
//Output: 5

// Round
x = Math.round(4.2);//Làm tròn đến số nguyên gần nhất
//Output: 4

// Round up
x = Math.ceil(4.2);//Làm tròn đến số nguyên lớn nhất
//Output: 5

// Round down
x = Math.floor(4.9);//Làm tròn đến số nguyên nhỏ nhất
// console.log(x);

// Exponent
x = Math.pow(2, 3);//sẽ trả về giá trị của 2 mũ 3

// Minimum number
x = Math.min(4, 5, 3);//sẽ trả về giá trị về giá trị nhỏ nhất
// console.log(x);

// Maximum number
x = Math.max(4, 5, 3);//sẽ trả về giá trị về giá trị lớn nhất

// Get a random number/decimal between 0 and 1
x = Math.random();//sẽ trả về giá trị về giá trị ngẫu nhiên

// Get a random number between 1 and 100
x = Math.floor(Math.random() * 100 + 1);//Trả về số nguyên ngẫu nhiên nhỏ nhất từ 1 đến 100

